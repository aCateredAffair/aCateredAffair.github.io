<b>Poached Salmon with Dilled Sour Cream and Cucumbers</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1 salmon fillet, approx 2 lbs, skin removed and bones pinned<br />
<b>&raquo;</b>
2 cups white wine<br />
<b>&raquo;</b>
6 springs fresh dill, 2 springs finely chopped<br />
<b>&raquo;</b>
1/2 cup sour cream<br />
<b>&raquo;</b>
1/2 cup cream cheese<br />
<b>&raquo;</b>
1 cucumber (or 1/2 an English Cucumber), thinly sliced<br />
<b>&raquo;</b>
Salt &amp; pepper to taste<br />
</p>

<p>
<em><b>Directions:</b></em><br />
<b>1.</b>
Combine sour cream, cream cheese and 2 sprigs of chopped dill in a bowl.  Set aside.<br />
<b>2.</b>
Place salmon in a poaching pan.  Add wine, 4 sprigs of dill, salt and pepper. The liquid should cover about 1/4" of the side of the fish. If it doesn't, add water.<br />
<b>3.</b>
Place the pan on medium low heat and bring the poaching liquid to a gentle simmer.<br />
<b>4.</b>
Poach salmon for approximately 20 minutes, or until barely firm.<br />
<b>5.</b>
Remove salmon from pan and place on serving platter. Spread dilled sour cream evenly on top of the salmon, arrange cucumbers on top of the sour cream.<br />
<b>6.</b>
Wrap platter in plastic wrap and refrigerate until serving.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
If you don't have a poaching pan, use the smallest covered pan that will fit the fish.
<br />
<b>&raquo;</b>
Salmon can be prepared a day ahead.
<br />
</p>

