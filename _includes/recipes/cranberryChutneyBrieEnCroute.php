<b>Cranberry Chutney Brie en Croute</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1/4 or a 2lb wheel of Brie, rind intact<br />
<b>&raquo;</b>
1/2 cup cranberry chutney<br />
<b>&raquo;</b>
1 sheet Athens puff pastry, thawed to room temperature<br />
<b>&raquo;</b>
1 egg, beaten with 2 tbsb water<br />
</p>

<p>
<em><b>Directions:</b></em><br />
<b>1.</b>
Pre-heat oven to 350F.<br />
<b>2.</b>
Place wedge of brie on a corner of the puff pastry, then cut around it with a paring knife.<br />
<b>3.</b>
Remove, but reserve the rest of the puff pastry sheet.<br />
<b>4.</b>
Spread cranberry chutney on top of the brie, then drape reserved puff pastry over the top. <br />
<b>5.</b>
Trim away excess puff pastry, leaving about 1/2" extra to tuck underneath.  Reserve pieces.<br />
<b>6.</b>
Carefully flip the brie over.  Brush the edges of the base puff pastry with the beaten egg. <br />
<b>7.</b>
Fold the top pastry over the base, and seal firmly.  Flip brie back over.<br />
<b>8.</b>
Brush entire surface with egg wash, then bake at 350F for 30 minutes.<br />
<b>9.</b>
Serve immediately, with crackers and/or bread.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
For a different flavor, use any type of chutney or jelly that you like.<br />
<b>&raquo;</b>
Puff pastry scraps can be used to decorate the top, if you wish.<br />
<b>&raquo;</b>
Can be prepared and refrigerated up to 3 days ahead.<br />
<b>&raquo;</b>
Can be prepared and frozen for up to 6 months. Allow to thaw under refrigeration (uncovered) for 24-36 hours before baking.<br />
</p>

