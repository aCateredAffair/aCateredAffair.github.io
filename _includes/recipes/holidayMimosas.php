<b>Holiday Mimosas</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1 bottle Mauby "Fizz" sparkling wine<br />
<b>&raquo;</b>
2 cups cranberry pomegranate juice<br />
</p>

<p>
<em><b>Directions:</b></em><br />
<b>1.</b>
Combine wine and juice in a pitcher.<br />
<b>2.</b>
Pour into champagne glasses and serve.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
If you like, you can prepare the drinks directly in the champagne glasses.<br />
<b>&raquo;</b>
If you prefer a sweeter drink, add orange juice, or use asti-spumante.<br />
<b>&raquo;</b>
Prepare this drink last minute so that it keeps it's fizz.<br />
</p>

