<b>Bite-Sized Beef Wellingtons</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1/2 lb mushrooms (preferably mixed varieties), chopped finely<br />
<b>&raquo;</b>
1 tbsp olive oil<br />
<b>&raquo;</b>
1/4 cup red wine<br />
<b>&raquo;</b>
1 lb beef fillet mingnon, cut into 1/2 inch cubes<br />
<b>&raquo;</b>
Seasoning of your choice (salt, peppper, garlic, lemon zest, etc.)<br />
<b>&raquo;</b>
4 sheets Athens puff pastry, thawed to room temperature<br />
<b>&raquo;</b>
1 egg, beaten with 2 tbsp of water<br />
</p>

<p>
<em><b>Directions:</b></em><br />
<b>1.</b>
Pre-heat oven to 350F.<br />
<b>2.</b>
Sautee chopped mushrooms in olive oil. When soft add red wine, and allow to reduce. When most of the liquid is gone, remove from heat and set aside.<br />
<b>3.</b>
Toss fillet cubes in seasoning, then saute over high heat until barely browned. Set aside.<br />
<b>4.</b>
Lay out two sheets of puff pastry, and brush with the beaten egg.<br />
<b>5.</b>
Arrange fillet cubes on the puff pastry in rows, with 1 1/2" spacing between them.<br />
<b>6.</b>
Gently lay remaining 2 pieces of pastry over the top of the meat and mushrooms. Beginning in the middle, ease the pastry together. Work your way to the outside seams, then go back and seal each seam firmly.  Cut wellingtons apart with a paring knife.<br />
<b>7.</b>
Brush entire surfaces with egg wash;  transfer to a lined baking sheet;  bake at 350F for 30 minutes.<br />
<b>8.</b>
Serve immediately.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
Beef Wellingtons can be prepared and refrigerated up to 1 day ahead of time.<br />
</p>

