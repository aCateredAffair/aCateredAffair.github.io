<b>Vegetable Tartlets</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1 box Athens fillo tartlet shells<br />
<b>&raquo;</b>
1 1/2 cups mixed vegetables, finely chopped<br />
<b>&raquo;</b>
1 tbsp olive oil<br />
<b>&raquo;</b>
2 tbsp white wine<br />
<b>&raquo;</b>
1/4 cup parmesan cheese, shredded<br />
<b>&raquo;</b>
Salt &amp; pepper to taste<br />
</p>

<p>
<em><b>Directions:</b></em><br />
<b>1.</b>
Preheat oven to 350F.
<br />
<b>2.</b>
Heat saut&eacute; pan, add olive oil, then saut&eacute; mixed vegetables. Add salt and pepper to taste.
<br />
<b>3.</b>
Deglaze pan with white wine, remove from heat and set aside.
<br />
<b>4.</b>
Place fillo tartlet shells on a lined baking sheet, fill with vegetable mixture, top with shredded parmesan cheese, then bake at 350F for 20 minutes.<br />
<b>5.</b>
Serve immediately.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
Filling can be made and refrigerated up to 3 days ahead.<br />
<b>&raquo;</b>
Any vegetables you like may be used, but use cabbage and tomatoes sparingly!<br />
</p>

