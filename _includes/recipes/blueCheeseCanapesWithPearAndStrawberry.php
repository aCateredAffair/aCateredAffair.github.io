<b>Blue Cheese Canapes w/Pear and Strawberry</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1 cup cream cheese, softened to room temperature<br />
<b>&raquo;</b>
1 cup blue cheese crumbles<br />
<b>&raquo;</b>
24 water crackers<br />
<b>&raquo;</b>
1 fresh pear<br />
<b>&raquo;</b>
6 fresh strawberries<br />
<b>&raquo;</b>
Salt &amp; pepper to taste<br />
</p>

<p>
<em><b>Directions:</b></em><br />
<b>1.</b>
Combine cream cheese and blue cheese in a mixing bowl.  Beat until fluffy.  Add salt and pepper to taste, if desired.<br />
<b>2.</b>
Put blue cheese mixture in a pastry bag with a large star tip.  Set aside.<br />
<b>3.</b>
Cut pears into thin wedges, approximately 1 1/2" long.<br />
<b>4.</b>
Trim strawberries and slice lengthwise into 1/8" slices.<br />
<b>5.</b>
Pipe a blue cheese rosettes onto water crackers.<br />
<b>6.</b>
Arrange pear and strawberry slices on the rosettes.<br />
<b>7.</b>
Serve immediately.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
Blue cheese mixture can be made several days ahead, but allow it to warm up before you try to pipe it onto the crackers.<br />
<b>&raquo;</b>
If you slice the pears ahead of time, dip them in lemon water to prevent browning.<br />
</p>

