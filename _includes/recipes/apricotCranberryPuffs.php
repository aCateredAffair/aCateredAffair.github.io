<b>Apricot Cranberry Puffs</b>

<p>
<em><b>Ingredients:</b></em><br />
<b>&raquo;</b>
1 cup dried apricots, chopped<br />
<b>&raquo;</b>
1 cup fresh cranberries, chopped<br />
<b>&raquo;</b>
1/2 cup brandy<br />
<b>&raquo;</b>
1/2 cup sugar<br />
<b>&raquo;</b>
1/4 cup butter<br />
<b>&raquo;</b>
1 tsp cinnamon<br />
<b>&raquo;</b>
2 sheets puff pastry, thawed and unfolded<br />
<b>&raquo;</b>
1 egg, beaten with 1 tbsp of water<br />
<b>&raquo;</b>
1/4 cup sanding sugar<br />
</p>

<p>
<em><b>Directions (Filling):</b></em><br />
<b>1.</b>
Soak the chopped apricots in the brandy for at least 1 hour.<br />
<b>2.</b>
Combine apricots, brandy, cranberries, butter, sugar, and cinnamon in a saucepan and simmer until tender, approximately 30-45 minutes.<br />
<b>3.</b>
Fill puffs (recipe below) with cranberry filling. Serve immediately.<br />
</p>

<p>
<em><b>Directions (Pastry Puffs):</b></em><br />
<b>1.</b>
Roll puff pastry sheets out to 1/8" thickness, cut into 2 inch squares. Cut a 1/2 inch slit into each side of each square.<br />
<b>2.</b>
Place the puff pastry squares into a greased mini muffin tin, overlapping at each slit.<br />
<b>3.</b>
Brush pastry with the egg wash, sprinkle with sanding sugar, and "dock" with a fork.<br />
<b>4.</b>
Bake at 350F for 30 minutes, or until golden.<br />
</p>

<p>
<em><b>Notes:</b></em><br />
<b>&raquo;</b>
Filling and pastry puffs can be made ahead of time, then assembled and baked to heat at serving time.<br />
<b>&raquo;</b>
To help keep the puff's shape, a second mini muffin tin can be stacked on top of the pastry when baking it.<br />
</p>

